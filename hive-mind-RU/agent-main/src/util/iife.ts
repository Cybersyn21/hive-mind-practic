// Утилита для немедленного вызова функции (Immediately Invoked Function Expression)
// Принимает функцию и сразу же вызывает её, возвращая результат
// Полезно для инициализации значений, которые требуют выполнения кода
export function iife<T>(fn: () => T) {
  // Вызываем переданную функцию и возвращаем её результат
  return fn()
}
