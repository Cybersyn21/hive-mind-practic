import { EventEmitter } from "events"

// Глобальная шина событий для передачи событий между разными инстансами проектов
// Используется для кросс-проектной коммуникации и синхронизации событий
export const GlobalBus = new EventEmitter<{
  event: [
    {
      directory: string // Директория проекта, из которого произошло событие
      payload: any // Полезная нагрузка события (может быть любым типом)
    },
  ]
}>()
